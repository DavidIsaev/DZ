import java.util.Scanner;

public class Exerciese_5{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int y = sc.nextInt();
        if (x+y>=10 & x+y<=20) System.out.println("true");
        else System.out.println("false");
        System.out.println();
    }
}